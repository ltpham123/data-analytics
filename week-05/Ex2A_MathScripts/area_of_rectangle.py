# Say you have a rectangle that has dimensions corresponding to your birthday – the
# month number is one side and the day of the month is the other side. How would
# you calculate the area of this rectangle?

# Side A is [number]
# Side B is [number]
# The area of the rectangle is [number]

side_a = 7  # Example month number (July)
side_b = 13  # Example day of the month
area = side_a * side_b

# Measurements are based in 'units'
print(f"Side A is {side_a}")
print(f"Side B is {side_b}")
print(f"The area of the rectangle is {area}")